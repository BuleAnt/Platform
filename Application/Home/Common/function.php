/**
 * 检测验证码
 * @param  integer $id 验证码ID
 * @return boolean     检测结果
 * @author 麦当苗儿 <zuojiazi@vip.qq.com>
 */
function check_verify($code, $id = 1){
	$verify = new \Think\Verify();
	return $verify->check($code, $id);
}

/**
 * 获取列表总行数
 * @param  string  $category 分类ID
 * @param  integer $status   数据状态
 * @author 麦当苗儿 <zuojiazi@vip.qq.com>
 */
function get_list_count($category, $status = 1){
    static $count;
    if(!isset($count[$category])){
        $count[$category] = D('Document')->listCount($category, $status);
    }
    return $count[$category];
}

/**
 * 获取段落总数
 * @param  string $id 文档ID
 * @return integer    段落总数
 * @author 麦当苗儿 <zuojiazi@vip.qq.com>
 */
function get_part_count($id){
    static $count;
    if(!isset($count[$id])){
        $count[$id] = D('Document')->partCount($id);
    }
    return $count[$id];
}

/**
 * 获取导航URL
 * @param  string $url 导航URL
 * @return string      解析或的url
 * @author 麦当苗儿 <zuojiazi@vip.qq.com>
 */
function get_nav_url($url){
    switch ($url) {
        case 'http://' === substr($url, 0, 7):
        case '#' === substr($url, 0, 1):
            break;        
        default:
            $url = U($url);
            break;
    }
    return $url;
}

/**
 * [get_file_field 获得文件信息]
 * @param  [type] $value [id值]
 * @param  [type] $field [查询字段值：path,mime,]
 * @return [type]        [结果]
 */
function get_file_field($value = null, $field = null){
    if(empty($value)){
        return false;
    }

    //拼接参数
    $map['id'] = $value;
    $info = M('File')->where($map);
    if($field != 'path'){
        if(empty($field)){
            $info = $info->field(true)->find();
        }else{
            $info = $info->getField($field);
        }
    } else {
       $setting = C('DOWNLOAD_UPLOAD');
       $data = $info->field(true)->find();
       $info = substr($setting['rootPath'], 1).$data['savepath'].$data['savename']; //在模板里的url路径
    }

    return $info;
}